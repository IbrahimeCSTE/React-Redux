import { DECREMENT, INCREMENT } from "./Type";

const init = {
  count: 5,
};
export const reducer = (state = init, action) => {
  switch (action.type) {
    case INCREMENT:
      return {
        ...state,
        count: state.count + 1,
      };
    case DECREMENT:
      return {
        ...state,
        count: state.count - 1,
      };
    default:
      return state;
  }
};
